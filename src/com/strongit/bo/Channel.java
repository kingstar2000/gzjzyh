package com.strongit.bo;

public class Channel {
	
	private String blockid;
	
	private String blocktype;
	
	private String blocktpl;
	
	private String blockimg;
	
	private String blocklayout;
	
	private String blocktitle;
	
	private String blockurl;

	public String getBlockurl() {
		return blockurl;
	}

	public void setBlockurl(String blockurl) {
		this.blockurl = blockurl;
	}

	public String getBlockid() {
		return blockid;
	}

	public void setBlockid(String blockid) {
		this.blockid = blockid;
	}

	public String getBlockimg() {
		return blockimg;
	}

	public void setBlockimg(String blockimg) {
		this.blockimg = blockimg;
	}

	public String getBlocklayout() {
		return blocklayout;
	}

	public void setBlocklayout(String blocklayout) {
		this.blocklayout = blocklayout;
	}

	public String getBlocktpl() {
		return blocktpl;
	}

	public void setBlocktpl(String blocktpl) {
		this.blocktpl = blocktpl;
	}

	public String getBlocktype() {
		return blocktype;
	}

	public void setBlocktype(String blocktype) {
		this.blocktype = blocktype;
	}

	public String getBlocktitle() {
		return blocktitle;
	}

	public void setBlocktitle(String blocktitle) {
		this.blocktitle = blocktitle;
	}
	
}
